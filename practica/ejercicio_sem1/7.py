#Escribe un programa que calcule el área de un triángulo a partir de la base y la
#altura dadas. base x altura // 2
print("\n")
base = float(input("Ingrese valor de la base: "))
print("\t")
altura = float(input("Ingrese valor de la altura: "))
print("\t")
area = (base * altura) / 2;
print("\t")
print("El área del círculo es:", area)
print("\t")